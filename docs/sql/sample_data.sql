    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    code VARCHAR(20) UNIQUE,
    name1 VARCHAR(50),
    name2 VARCHAR(50), 
    name3 VARCHAR(50),
    nx INTEGER,
    ny INTEGER,
    lon_total DOUBLE,
